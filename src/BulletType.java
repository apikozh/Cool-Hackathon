public class BulletType {
    private int delay;
    private int damage;
    private int radius;

    public int getDelay() {
        return delay;
    }

    public int getDamage() {
        return damage;
    }

    public int getRadius() {
        return radius;
    }

    public void setDelay(int delay) {
        this.delay = delay;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }
}
